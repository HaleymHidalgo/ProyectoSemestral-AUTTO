    private void actualizarTabla(){
        // Declaraciones Necesarias
        CtrlReportes con = new CtrlReportes();
        ArrayList<ArrayList<Object>> lista = con.buscarClientesTOP();
        DefaultTableModel modelo = (DefaultTableModel) tblReporte.getModel();
        modelo.setRowCount(0);
        for (ArrayList<Object> datosEmpleado : lista) {
            String rutVendedor = (String) datosEmpleado.get(0);
            int cantidadArriendos = (int) datosEmpleado.get(1);
            System.out.println(rutVendedor);
            // Agregar fila al modelo
            modelo.addRow(new Object[]{rutVendedor, cantidadArriendos});
        }
        System.out.println(lista);
        tblReporte.setModel(modelo);
    }

    
